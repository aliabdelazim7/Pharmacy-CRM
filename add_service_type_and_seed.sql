-- ----------------------------------------------------------------------------
-- 1) عمود النوع: 'product' = منتج له مخزون وسعر شراء | 'service' = خدمة بلا كمية
--    الخدمات نعطيها مخزوناً كبيراً جداً (1,000,000) حتى لا "تنفد" أبداً،
--    والتطبيق يعرض كلمة "خدمة" بدل الرقم ويخفي حقول الكمية وسعر الشراء.
-- ----------------------------------------------------------------------------
alter table products add column if not exists type text not null default 'product';

-- ----------------------------------------------------------------------------
-- 2) تصنيفات الباربر شوب (تُضاف بدون تكرار)
-- ----------------------------------------------------------------------------
insert into categories (name) values
  ('قص وحلاقة'),
  ('عناية وتنظيف'),
  ('صبغة وبروتين'),
  ('منتجات العناية')
on conflict do nothing;

-- ----------------------------------------------------------------------------
-- 3) الخدمات (type='service') — بلا سعر شراء، ومخزون لا نهائي عملياً
--    سعر البيع = سعر تقديم الخدمة فقط.
-- ----------------------------------------------------------------------------
insert into products (name, type, sale_price, purchase_price, average_purchase_price, stock_quantity, display_quantity, unit, category_id)
select v.name, 'service', v.price, 0, 0, 1000000, 1000000, 'قطعة',
       (select id from categories where name = v.cat limit 1)
from (values
  ('قص شعر رجالي',        50,  'قص وحلاقة'),
  ('حلاقة ذقن',           30,  'قص وحلاقة'),
  ('قص شعر + ذقن',        70,  'قص وحلاقة'),
  ('تحديد ذقن',           20,  'قص وحلاقة'),
  ('حلاقة أطفال',         40,  'قص وحلاقة'),
  ('حمام كريم',           40,  'عناية وتنظيف'),
  ('استشوار وتصفيف',      35,  'عناية وتنظيف'),
  ('تنظيف بشرة',          80,  'عناية وتنظيف'),
  ('إزالة شعر بالشمع',    25,  'عناية وتنظيف'),
  ('صبغة شعر',           120,  'صبغة وبروتين'),
  ('بروتين شعر',         300,  'صبغة وبروتين'),
  ('فرد وكيراتين',       350,  'صبغة وبروتين')
) as v(name, price, cat)
where not exists (select 1 from products p where p.name = v.name);

-- ----------------------------------------------------------------------------
-- 4) منتجات (type='product') — لها مخزون وسعر شراء وسعر بيع
-- ----------------------------------------------------------------------------
insert into products (name, type, sale_price, purchase_price, average_purchase_price, stock_quantity, display_quantity, unit, category_id)
select v.name, 'product', v.sale, v.buy, v.buy, v.qty, v.qty, 'قطعة',
       (select id from categories where name = 'منتجات العناية' limit 1)
from (values
  ('جل تثبيت الشعر',   80,  50, 20),
  ('واكس شعر',        120,  80, 15),
  ('سيروم لحية',      150, 100, 10),
  ('شامبو',            90,  60, 25),
  ('مشط حلاقة',        25,  12, 30),
  ('زيت لحية',        130,  85, 12)
) as v(name, sale, buy, qty)
where not exists (select 1 from products p where p.name = v.name);

